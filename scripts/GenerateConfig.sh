#!/usr/bin/env bash
# scripts/GenerateConfig.sh
# Resolve URLs from env / defaults and write runtime config files.
# Idempotent – safe to run repeatedly.
set -euo pipefail

REPO_ROOT="$(git rev-parse --show-toplevel)"
ENV_FILE="${REPO_ROOT}/.env"

# Load .env
if [ -f "$ENV_FILE" ]; then
  set -a; source "$ENV_FILE"; set +a
else
  echo "[WARN] No .env file found – using environment as-is."
fi

# Run validation first
bash "${REPO_ROOT}/scripts/ValidateEnv.sh"

echo ""
echo "==> Generating runtime config files..."

# ── Helpers ──────────────────────────────────────────────────────────────────
MODE="${OPENRPORT_DEPLOYMENT_MODE:-subpath}"
SERVER_BASE="${OPENRPORT_SERVER_BASE_PATH:-/}"
PAIRING_BASE="${OPENRPORT_PAIRING_BASE_PATH:-/pairing}"
UI_BASE="${OPENRPORT_UI_BASE_PATH:-/ui}"
SERVER_PORT="${OPENRPORT_SERVER_API_PORT:-8080}"
PAIRING_PORT="${OPENRPORT_PAIRING_PORT:-9978}"
UI_PORT="${OPENRPORT_UI_PORT:-3000}"

# Resolve public URL: env var wins, otherwise build from mode/host
resolve_url() {
  local explicit="$1" fallback="$2"
  if [[ -n "$explicit" ]]; then
    echo "$explicit"
  else
    echo "$fallback"
  fi
}

SERVER_URL="$(resolve_url "${OPENRPORT_SERVER_PUBLIC_URL:-}"  "http://localhost:${SERVER_PORT}")"
PAIRING_URL="$(resolve_url "${OPENRPORT_PAIRING_PUBLIC_URL:-}" "http://localhost:${PAIRING_PORT}")"
UI_URL="$(resolve_url "${OPENRPORT_UI_PUBLIC_URL:-}"      "http://localhost:${UI_PORT}")"

# In subpath mode, pairing URL is server_url + pairing_base
if [[ "$MODE" == "subpath" && -z "${OPENRPORT_PAIRING_PUBLIC_URL:-}" ]]; then
  PAIRING_URL="${SERVER_URL%/}${PAIRING_BASE}"
fi

echo "  Deployment mode : $MODE"
echo "  Server URL      : $SERVER_URL"
echo "  Pairing URL     : $PAIRING_URL"
echo "  UI URL          : $UI_URL"

# ── Config/Server/rportd.conf ─────────────────────────────────────────────
mkdir -p "${REPO_ROOT}/Config/Server"
RPORTD_CONF="${REPO_ROOT}/Config/Server/rportd.conf"

cat > "$RPORTD_CONF" <<EOF
# Generated by GenerateConfig.sh – do not edit manually.
# Re-run: make generate-config

[server]
address = "0.0.0.0:8081"
data_dir = "/var/lib/rport"
pairing_url = "${PAIRING_URL}"
auth = "${RPORTD_CLIENT_AUTH:-clientauth1:1234}"

[api]
address = "0.0.0.0:8080"
auth = "${RPORTD_API_USER:-admin}:${RPORTD_API_PASSWORD:-changeme}"
cors = ["${RPORTD_CORS_ORIGINS:-*}"]

# Header discovery
trust_proxy = ${OPENRPORT_TRUST_PROXY_HEADERS:-true}
EOF

echo "  Wrote: Config/Server/rportd.conf"

# ── Config/Pairing/config.toml ───────────────────────────────────────────────
mkdir -p "${REPO_ROOT}/Config/Pairing"
PAIRING_CONF="${REPO_ROOT}/Config/Pairing/config.toml"

cat > "$PAIRING_CONF" <<EOF
# Generated by GenerateConfig.sh – do not edit manually.
# Re-run: make generate-config

[server]
  address = "0.0.0.0:9978"
  url = "${PAIRING_URL}"
EOF

echo "  Wrote: Config/Pairing/config.toml"

# ── Config/Ui/runtime.env ────────────────────────────────────────────────────
mkdir -p "${REPO_ROOT}/Config/Ui"
UI_ENV="${REPO_ROOT}/Config/Ui/runtime.env"

cat > "$UI_ENV" <<EOF
# Generated by GenerateConfig.sh – do not edit manually.
NUXT_API_URL=${SERVER_URL}
NUXT_APP_BASE_URL=${UI_BASE}
EOF

echo "  Wrote: Config/Ui/runtime.env"

echo ""
echo "==> Config generation complete."
